package org.inter;

public class cha4 {

	public static void main(String[] args) {

		for (int i = 1; i <= 100; i++) {

			if (i % 2 == 0) {
				System.out.println("Even number " + i + " ");
			}
		}

		for (int i = 1; i <= 100; i++) {
			if (i % 2 != 0) {
				System.out.println("Odd number " + i + " ");
			}
		}

	}

}
